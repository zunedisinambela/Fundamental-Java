public class Deklarasi {
    public static void main(String[] args) {
        int jumlahUang;

        jumlahUang = 1000;
        System.out.println("Jumlah uang semula: " + jumlahUang);

        // Variable diubah
        jumlahUang = 1200;
        System.out.println("Jumlah uang sekarang: " + jumlahUang);
    }
}
