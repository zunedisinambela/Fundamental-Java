public class stirngRelasional {
    public static void main(String[] args) {
        String jawaban = "ya";

        System.out.print("Ya == ya -> ");
        if (jawaban == "ya")
            System.out.println("benar");
        else
            System.out.println("salah");

        System.out.print("Ya == Ya -> ");
        if (jawaban == "Ya")
            System.out.println("benar");
        else
            System.out.println("salah");
    }
}
